#include <stdint.h>
#include <limits.h>
#include <stdarg.h>
#include <stddef.h>
#include <sys/types.h>




void *memchr(const void *src, int c, size_t n) {

	for(int i = 0; i < n; i++) {
	
		if(((const char*) src) [i] == (char) c)
			return (void*) &((char*) src) [i];

	}

	return NULL;
	
}
